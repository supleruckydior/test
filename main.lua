local function AutostartDungeonf()
    -- Phase 1: Wait for victory to appear (with timeout)
    local victoryFound = false
    local waitStart = os.time()
    
    while not victoryFound and (os.time() - waitStart < 30) do -- 30 second timeout
        victoryFound = CheckDungeonVictory()
        if not victoryFound then
            wait(0.5) -- Check every 0.5 seconds
        end
    end
    
    if not victoryFound then return end -- Exit if no victory detected
    
    -- Phase 2: Process dungeon completion
    local currentKeys = 0
    local dungeonName = "Unknown"
    
    -- Safely get dungeon info
    pcall(function()
        local levelText = playerGui.GUI:WaitForChild("主界面")
                          :WaitForChild("战斗")
                          :WaitForChild("关卡信息")
                          :WaitForChild("文本").Text
        dungeonName = string.match(levelText, "^(.-)%s%d") or "Unknown"
        
        local keyType = ({
            ["Ore Dungeon"] = "OreDungeon",
            ["Gem Dungeon"] = "GemDungeon",
            ["Rune Dungeon"] = "RuneDungeon",
            ["Relic Dungeon"] = "RelicDungeon",
            ["Hover Dungeon"] = "HoverDungeon",
            ["Gold Dungeon"] = "GoldDungeon"
        })[dungeonName]
        
        if keyType then
            currentKeys = tonumber(getDungeonKey(keyType)) or 0
        end
    end)
    
    -- Phase 3: Level increase logic
    if AutoDungeonplus1 then
        adjustDungeonLevel(1)
        wait(1) -- Ensure level saves
    end
    
    -- Phase 4: Clean up victory UI
    ClearVictoryUI()
    
    -- Phase 5: Restart or switch dungeon
    wait(savemodetime2)
    teleporthome()
    wait(0.5)
    
    if Autofinishdungeon and currentKeys == 0 then
        print("自動切換到鑰匙最多的地下城")
        selectDungeonWithMostKeys()
    else
        DungeonTP()
    end
end

