-- 安全数值转换器
local function parseNumber(text)
    local str = tostring(text):lower():gsub("%s+", ""):gsub(",", "")
    local numStr = str:gsub("[^%d%.]", "")
    
    if countSubstring(numStr, "%.") > 1 then
        warn("[数值异常] 非法格式:", text)
        return 0
    end
    
    local multiplier = 1
    if str:find("k") then
        multiplier = 1000
    elseif str:find("m") then
        multiplier = 1000000
    end
    
    return (tonumber(numStr) or 0) * multiplier
end

