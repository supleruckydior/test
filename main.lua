            herbprint = false
            lowcontrol = false  -- Reset the control flag
            print(playerName.." --- 系统重置! (剩余"..currentHerbs.."草药)")
        end
        if herbprint and hasExecutedTrade then
        print(playerName.." --- "..currentHerbs.."草药")
         end
        -- Regular status print when in high herb mode
        
        wait(5)
    end
end

